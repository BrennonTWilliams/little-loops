class CodexEmitter:
    """Output emitter for the Codex host (``--host codex``).

    Each ``emit_*`` method receives a metadata dict built by the core traversal
    functions and returns ``"adapted"``, ``"skipped"``, or ``"error"``.
    """

    name = "codex"

    def emit_skill(self, skill_meta: dict) -> str:
        """Add Codex Skills API frontmatter to one SKILL.md."""
        skill_name: str = skill_meta["skill_name"]
        skill_path: Path = skill_meta["skill_path"]
        content: str = skill_meta["content"]
        apply: bool = skill_meta["apply"]
        quiet: bool = skill_meta["quiet"]

        short_desc = _extract_skill_short_desc(content)
        if not short_desc:
            if not quiet:
                print(f"  SKIP   {skill_name}: no description found")
            return "skipped"

        new_text, skill_changed = _insert_skill_fields(content, skill_name, short_desc)
        openai_yaml = skill_path.parent / "agents" / "openai.yaml"
        yaml_exists = openai_yaml.exists()

        if not skill_changed and yaml_exists:
            if not quiet:
                print(f"  SKIP   {skill_name}: already adapted")
            return "skipped"

        if apply:
            if skill_changed:
                skill_path.write_text(new_text)
            if not yaml_exists:
                openai_yaml.parent.mkdir(exist_ok=True)
                openai_yaml.write_text(
                    _make_openai_yaml_content(_title_case(skill_name), short_desc)
                )
            if not quiet:
                print(f"  APPLY  {skill_name}: {short_desc[:50]}")
        else:
            if not quiet:
                print(f"  DRY    {skill_name}: {short_desc[:50]}")

        return "adapted"

    def emit_command(self, cmd_meta: dict) -> str:
        """Bridge one command into a Codex-discoverable skills/ll-<stem>/ wrapper."""
        stem: str = cmd_meta["stem"]
        label = f"ll-{stem}"
        content: str = cmd_meta["content"]
        fm: dict = cmd_meta["fm"]
        output_dir: Path = cmd_meta["output_dir"]
        apply: bool = cmd_meta["apply"]
        quiet: bool = cmd_meta["quiet"]

        description = fm.get("description", "") or ""
        if not isinstance(description, str) or not description.strip():
            if not quiet:
                print(f"  SKIP   {label}: no description in frontmatter")
            return "skipped"

        short_desc = _extract_skill_short_desc(content)
        if not short_desc:
            if not quiet:
                print(f"  SKIP   {label}: empty short description")
            return "skipped"

        out_dir = output_dir / f"ll-{stem}"
        out_skill_md = out_dir / "SKILL.md"
        out_openai_yaml = out_dir / "agents" / "openai.yaml"

        if out_skill_md.exists() and out_openai_yaml.exists():
            if not quiet:
                print(f"  SKIP   {label}: already adapted")
            return "skipped"

        if apply:
            out_dir.mkdir(parents=True, exist_ok=True)
            if not out_skill_md.exists():
                out_skill_md.write_text(_synthesized_skill_md(stem, description))
            if not out_openai_yaml.exists():
                out_openai_yaml.parent.mkdir(exist_ok=True)
                out_openai_yaml.write_text(_make_openai_yaml_content(_title_case(stem), short_desc))
            if not quiet:
                print(f"  APPLY  {label}: {short_desc[:50]}")
        else:
            if not quiet:
                print(f"  DRY    {label}: {short_desc[:50]}")

        return "adapted"

    def emit_agent(self, agent_meta: dict) -> str:
        """Emit ``.codex/agents/<name>.toml`` for one agent definition."""
        agent_name: str = agent_meta["agent_name"]
        content: str = agent_meta["content"]
        fm: dict = agent_meta["fm"]
        output_dir: Path = agent_meta["output_dir"]
        apply: bool = agent_meta["apply"]
        quiet: bool = agent_meta["quiet"]

        if not fm:
            if not quiet:
                print(f"  SKIP   {agent_name}: no parseable frontmatter")
            return "skipped"

        name = str(fm.get("name") or agent_name)
        model = str(fm.get("model") or "")
        short_desc = _extract_agent_short_desc(content)
        if not short_desc:
            if not quiet:
                print(f"  SKIP   {agent_name}: no description found")
            return "skipped"

        body = _extract_body(content)
        out_toml = output_dir / f"{agent_name}.toml"
        new_content = _format_agent_toml(name, short_desc, model, body, fm)

        if out_toml.exists():
            existing = out_toml.read_text()
            if not any(existing.startswith(m) for m in _LL_GENERATED_MARKERS):
                if not quiet:
                    print(f"  SKIP   {agent_name}: user-authored file (no marker)")
                return "skipped"
            if existing == new_content:
                if not quiet:
                    print(f"  SKIP   {agent_name}: already up to date")
                return "skipped"

        if apply:
            output_dir.mkdir(parents=True, exist_ok=True)
            out_toml.write_text(new_content)
            if not quiet:
                print(f"  APPLY  {agent_name}: {short_desc[:50]}")
        else:
            if not quiet:
                print(f"  DRY    {agent_name}: {short_desc[:50]}")

        return "adapted"
