# ---------------------------------------------------------------------------
# Rich TOML field derivation (ENH-2121 absorbed into FEAT-2391)
# ---------------------------------------------------------------------------


def _derive_sandbox_mode(tools: list | None) -> str | None:
    """Derive Codex ``sandbox_mode`` from the agent's tools list.

    Returns ``"read-only"`` when all tools are known read-only operations,
    ``"write-to-cwd"`` when any write tool is present, or ``None`` when the
    tools list is absent (field omitted; Codex inherits from the session).
    """
    if not tools:
        return None
    tool_set = set(tools)
    if not (tool_set & _WRITE_TOOLS):
        return "read-only"
    return "write-to-cwd"


def _derive_mcp_servers(tools: list | None) -> list[str] | None:
    """Derive Codex ``mcp_servers`` from MCP tool references in the tools list.

    Returns a deduplicated list of server names extracted from ``mcp__<server>__*``
    patterns, or ``None`` when no MCP tools are present.
    """
    if not tools:
        return None
    servers: list[str] = []
    for tool in tools:
        if isinstance(tool, str) and tool.startswith("mcp__"):
            parts = tool.split("__")
            if len(parts) >= 2:
                server = parts[1]
                if server not in servers:
                    servers.append(server)
    return servers if servers else None


def _format_agent_toml(
    name: str,
    description: str,
    model: str,
    body: str,
    fm: dict,
) -> str:
    """Return TOML content for ``.codex/agents/<name>.toml``.

    Emits the four core fields plus optional rich fields derived from *fm*
    (``sandbox_mode``, ``mcp_servers``) when source data is available.
    """
    escaped_desc = description.replace("\\", "\\\\").replace('"', '\\"')
    escaped_model = model.replace("\\", "\\\\").replace('"', '\\"')
    safe_body = body.replace('"""', '\\"\\"\\"')
    if not safe_body.endswith("\n"):
        safe_body += "\n"

    result = (
        f'{_MARKER}\nname = "{name}"\ndescription = "{escaped_desc}"\nmodel = "{escaped_model}"\n'
    )

    # Resolve tools from frontmatter for rich-field derivation
    tools = fm.get("tools")
    if isinstance(tools, str):
        try:
            tools = yaml.safe_load(tools) or []
        except yaml.YAMLError:
            tools = []

    sandbox_mode = _derive_sandbox_mode(tools)
    if sandbox_mode:
        result += f'sandbox_mode = "{sandbox_mode}"\n'

    mcp_servers = _derive_mcp_servers(tools)
    if mcp_servers:
        servers_str = ", ".join(f'"{s}"' for s in mcp_servers)
        result += f"mcp_servers = [{servers_str}]\n"

    result += f'\ndeveloper_instructions = """\n{safe_body}"""\n'
    return result


