from __future__ import annotations

import re
from pathlib import Path

import yaml

from little_loops.adapters.core import AdapterError, _extract_body  # noqa: F401

__all__ = ["CodexEmitter"]

_MARKER = "# generated by ll-adapt"
_MAX_SHORT_DESC = 80
_FM_CLOSE_RE = re.compile(r"\n---\s*\n")

# Recognised ll-generated markers for idempotency; includes the legacy value so
# existing files are not treated as user-authored and can be updated in place.
_LL_GENERATED_MARKERS = (
    _MARKER,
    "# generated by ll-adapt-agents-for-codex",
)

_READ_ONLY_TOOLS = frozenset({"Read", "Glob", "Grep", "WebFetch", "WebSearch"})
_WRITE_TOOLS = frozenset({"Edit", "Write", "Bash", "NotebookEdit"})


# ---------------------------------------------------------------------------
# Skill helpers (migrated from adapt_skills_for_codex.py)
# ---------------------------------------------------------------------------


def _extract_skill_short_desc(text: str) -> str:
    """Parse skill/command content and return first non-empty description line, ≤80 chars."""
    if not text.startswith("---"):
        return ""
    end = text.find("---", 3)
    if end == -1:
        return ""
    fm_raw = text[3:end]
    try:
        fm = yaml.safe_load(fm_raw) or {}
    except yaml.YAMLError:
        return ""
    desc = fm.get("description", "") or ""
    if not isinstance(desc, str):
        return ""
    for line in desc.splitlines():
        stripped = line.strip()
        if stripped:
            return stripped[:_MAX_SHORT_DESC]
    return ""


def _insert_skill_fields(content: str, name: str, short_desc: str) -> tuple[str, bool]:
    """Insert ``name:`` and ``metadata.short-description:`` into SKILL.md frontmatter.

    Uses targeted string manipulation — no yaml roundtrip — to preserve
    existing frontmatter formatting.  Returns ``(new_content, changed)``.
    """
    if not content.startswith("---\n"):
        return content, False

    m = _FM_CLOSE_RE.search(content[3:])
    if not m:
        return content, False

    fm_text = content[4 : 3 + m.start()]
    after = content[3 + m.start() :]

    changed = False

    if not re.search(r"^name\s*:", fm_text, re.MULTILINE):
        fm_text = f"name: {name}\n" + fm_text
        changed = True

    if "short-description:" not in fm_text:
        if re.search(r"^metadata\s*:", fm_text, re.MULTILINE):
            fm_text = re.sub(
                r"^(metadata\s*:.*)$",
                lambda mtch: mtch.group(0) + f"\n  short-description: {short_desc}",
                fm_text,
                flags=re.MULTILINE,
                count=1,
            )
        else:
            fm_text += f"\nmetadata:\n  short-description: {short_desc}"
        changed = True

    return f"---\n{fm_text}{after}", changed


def _title_case(slug: str) -> str:
    """Convert a kebab-case slug to title-cased display name."""
    return " ".join(word.capitalize() for word in slug.replace("-", " ").split())


def _make_openai_yaml_content(display_name: str, short_desc: str) -> str:
    """Return ``agents/openai.yaml`` content for the Codex Skills API."""
    return f'interface:\n  display_name: "{display_name}"\n  short_description: "{short_desc}"\n'


def _synthesized_skill_md(stem: str, description: str) -> str:
    """Build a minimal synthesized SKILL.md for a bridged command."""
    short_desc = ""
    for line in description.splitlines():
        stripped = line.strip()
        if stripped:
            short_desc = stripped[:_MAX_SHORT_DESC]
            break

    if "\n" in description.strip():
        indented = "\n".join(f"  {line}" if line else "" for line in description.splitlines())
        desc_block = f"description: |\n{indented}"
    else:
        desc_block = f"description: {description.strip()}"

    return (
        f"---\n"
        f"name: ll-{stem}\n"
        f"{desc_block}\n"
        # Bridge stubs exist only for Codex Skills API discovery (which reads the
        # agents/openai.yaml sidecar); hide them from the Claude Code skill
        # listing budget — users invoke the /ll:<stem> slash command (ENH-1615).
        f"disable-model-invocation: true\n"
        f"metadata:\n"
        f"  short-description: {short_desc}\n"
        f"---\n"
        f"\n"
        f"# {_title_case(stem)}\n"
        f"\n"
        f"Bridged from `commands/{stem}.md` for Codex Skills API discovery.\n"
        f"See the source command file for the full prompt body.\n"
    )


