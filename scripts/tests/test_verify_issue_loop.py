"""Tests for /ll:verify-issue-loop skill artifacts.

Since /ll:verify-issue-loop is a prompt-based skill (markdown instructions
for Claude), we test:

1. Expected emitted YAML structure for a multi-criterion issue
2. Linear pass-routing: on_yes chains forward, final on_yes -> done
3. Shared failure routing: every verify state's on_no -> failed
4. Both done and failed are terminal: true
5. Generated YAML passes FSM validation via load_and_validate + validate_fsm
"""

from __future__ import annotations

import sys
from pathlib import Path

import pytest
import yaml

from little_loops.fsm.validation import ValidationSeverity, load_and_validate, validate_fsm

