# ---------------------------------------------------------------------------
# Structural Tests — single-issue verification loop shape
# ---------------------------------------------------------------------------


class TestVerifyLoopSingleIssue:
    """Structural tests for a single-issue verification loop (3 criteria)."""

    @pytest.fixture
    def loop(self) -> dict:
        return yaml.safe_load(VERIFY_YAML_3_CRITERIA)

    def test_parses_as_yaml(self, loop: dict) -> None:
        assert isinstance(loop, dict)

    def test_initial_state_is_first_verify_criterion(self, loop: dict) -> None:
        assert loop.get("initial") == "verify-criterion-1"

    def test_category_is_verification(self, loop: dict) -> None:
        assert loop.get("category") == "verification"

    def test_has_all_verify_criterion_states(self, loop: dict) -> None:
        states = loop.get("states", {})
        for n in (1, 2, 3):
            assert f"verify-criterion-{n}" in states, f"missing verify-criterion-{n}"

    def test_verify_states_are_prompt_type(self, loop: dict) -> None:
        states = loop["states"]
        for n in (1, 2, 3):
            state = states[f"verify-criterion-{n}"]
            assert state.get("action_type") == "prompt"

    def test_verify_states_have_llm_structured_evaluator(self, loop: dict) -> None:
        states = loop["states"]
        for n in (1, 2, 3):
            evaluate = states[f"verify-criterion-{n}"].get("evaluate", {})
            assert evaluate.get("type") == "llm_structured"

    def test_verify_state_prompts_are_substantive(self, loop: dict) -> None:
        states = loop["states"]
        for n in (1, 2, 3):
            prompt = states[f"verify-criterion-{n}"].get("evaluate", {}).get("prompt", "")
            assert len(prompt) > 20, f"llm_structured prompt for criterion {n} too short"

    def test_non_final_states_route_on_yes_to_next_criterion(self, loop: dict) -> None:
        states = loop["states"]
        assert states["verify-criterion-1"].get("on_yes") == "verify-criterion-2"
        assert states["verify-criterion-2"].get("on_yes") == "verify-criterion-3"

    def test_final_state_routes_on_yes_to_done(self, loop: dict) -> None:
        states = loop["states"]
        assert states["verify-criterion-3"].get("on_yes") == "done"

    def test_every_verify_state_routes_on_no_to_failed(self, loop: dict) -> None:
        states = loop["states"]
        for n in (1, 2, 3):
            assert states[f"verify-criterion-{n}"].get("on_no") == "failed"

    def test_has_terminal_done_state(self, loop: dict) -> None:
        states = loop.get("states", {})
        assert "done" in states
        assert states["done"].get("terminal") is True

    def test_has_terminal_failed_state(self, loop: dict) -> None:
        states = loop.get("states", {})
        assert "failed" in states
        assert states["failed"].get("terminal") is True

    def test_no_discover_state(self, loop: dict) -> None:
        """Single-issue scope — no multi-issue discover/advance states."""
        assert "discover" not in loop.get("states", {})

    def test_no_advance_state(self, loop: dict) -> None:
        """Single-issue scope — no multi-issue discover/advance states."""
        assert "advance" not in loop.get("states", {})

    def test_no_check_invariants(self, loop: dict) -> None:
        """Verification loops do not include check_invariants (diff size gate)."""
        assert "check_invariants" not in loop.get("states", {})

    def test_no_check_stall(self, loop: dict) -> None:
        assert "check_stall" not in loop.get("states", {})


# ---------------------------------------------------------------------------
# Validation Tests — generated YAML passes FSM validation
# ---------------------------------------------------------------------------


class TestVerifyLoopValidation:
    """Tests that generated YAML passes ll-loop validate (via load_and_validate)."""

    @pytest.fixture
    def loops_dir(self, tmp_path: Path) -> Path:
        loops = tmp_path / ".loops"
        loops.mkdir()
        return loops

    def test_passes_fsm_validation(
        self, loops_dir: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        """3-criterion verify YAML passes FSM structural validation."""
        path = loops_dir / "verify-feat-919-add-json-schema-generation.yaml"
        path.write_text(VERIFY_YAML_3_CRITERIA)
        monkeypatch.chdir(loops_dir.parent)

        fsm, _ = load_and_validate(path)
        errors = validate_fsm(fsm)
        hard_errors = [e for e in errors if e.severity == ValidationSeverity.ERROR]
        assert not hard_errors, f"FSM errors: {[str(e) for e in hard_errors]}"

    def test_passes_via_cli(self, loops_dir: Path, monkeypatch: pytest.MonkeyPatch) -> None:
        """3-criterion verify YAML passes ll-loop validate CLI (exit 0)."""
        name = "verify-feat-919-add-json-schema-generation"
        (loops_dir / f"{name}.yaml").write_text(VERIFY_YAML_3_CRITERIA)
        monkeypatch.chdir(loops_dir.parent)

        with pytest.MonkeyPatch.context() as mp:
            mp.setattr(sys, "argv", ["ll-loop", "validate", name])
            from little_loops.cli import main_loop

            result = main_loop()

        assert result == 0
