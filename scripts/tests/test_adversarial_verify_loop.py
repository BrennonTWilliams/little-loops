"""Tests for /ll:adversarial-verify-loop skill artifacts.

Since /ll:adversarial-verify-loop is a prompt-based skill (markdown instructions
for Claude), we test:

1. Expected emitted YAML structure: 3 probe states + count_probes shell gate
2. Routing: each probe's on_yes chains forward, final probe → count_probes → done
3. Failure routing: every probe's on_no → failed_with_finding
4. count_probes uses output_numeric (non-LLM, MR-1 complement) with target 3
5. Three terminal states: done, failed_with_finding, failed_too_few
6. Generated YAML passes FSM validation via load_and_validate + validate_fsm
"""

