class TestAdversarialLoopStructure:
    """Structural tests for the adversarial verification loop."""

    @pytest.fixture
    def loop(self) -> dict:
        return yaml.safe_load(ADVERSARIAL_YAML)

    def test_parses_as_yaml(self, loop: dict) -> None:
        assert isinstance(loop, dict)

    def test_initial_state_is_probe_boundary(self, loop: dict) -> None:
        assert loop.get("initial") == "probe-boundary"

    def test_category_is_verification(self, loop: dict) -> None:
        assert loop.get("category") == "verification"

    def test_has_all_probe_states(self, loop: dict) -> None:
        states = loop.get("states", {})
        for name in ("probe-boundary", "probe-malformed-hostile", "probe-failure-mode"):
            assert name in states, f"missing probe state: {name}"

    def test_has_count_probes_state(self, loop: dict) -> None:
        assert "count_probes" in loop.get("states", {})

    def test_probe_states_are_prompt_type(self, loop: dict) -> None:
        states = loop["states"]
        for name in ("probe-boundary", "probe-malformed-hostile", "probe-failure-mode"):
            assert states[name].get("action_type") == "prompt", f"{name} must be action_type: prompt"

    def test_probe_states_have_llm_structured_evaluator(self, loop: dict) -> None:
        states = loop["states"]
        for name in ("probe-boundary", "probe-malformed-hostile", "probe-failure-mode"):
            evaluate = states[name].get("evaluate", {})
            assert evaluate.get("type") == "llm_structured", f"{name} must use llm_structured evaluator"

    def test_probe_state_evaluator_prompts_are_substantive(self, loop: dict) -> None:
        states = loop["states"]
        for name in ("probe-boundary", "probe-malformed-hostile", "probe-failure-mode"):
            prompt = states[name].get("evaluate", {}).get("prompt", "")
            assert len(prompt) > 20, f"llm_structured prompt for {name} too short"

    def test_probe_boundary_routes_on_yes_to_malformed(self, loop: dict) -> None:
        states = loop["states"]
        assert states["probe-boundary"].get("on_yes") == "probe-malformed-hostile"

