class TestAdversarialLoopRoutingScenarios:
    """Tests that all three outcome scenarios have correct routing."""

    def test_break_found_variant_has_failed_with_finding_terminal(self) -> None:
        """Variant representing a break found: failed_with_finding must be terminal."""
        loop = yaml.safe_load(ADVERSARIAL_YAML_BREAK_FOUND)
        states = loop.get("states", {})
        assert "failed_with_finding" in states
        assert states["failed_with_finding"].get("terminal") is True

    def test_too_few_variant_has_failed_too_few_terminal(self) -> None:
        """Variant representing too-few probes: failed_too_few must be terminal."""
        loop = yaml.safe_load(ADVERSARIAL_YAML_TOO_FEW)
        states = loop.get("states", {})
        assert "failed_too_few" in states
        assert states["failed_too_few"].get("terminal") is True

    def test_all_probes_pass_path_ends_at_done(self) -> None:
        """Happy path: probe-boundary→probe-malformed-hostile→probe-failure-mode→count_probes→done."""
        loop = yaml.safe_load(ADVERSARIAL_YAML)
        states = loop["states"]
        # Walk the on_yes chain
        path = []
        state = "probe-boundary"
        for _ in range(10):
            path.append(state)
            nxt = states[state].get("on_yes")
            if nxt is None or states.get(nxt, {}).get("terminal"):
                if nxt:
                    path.append(nxt)
                break
            state = nxt
        assert path == [
            "probe-boundary",
            "probe-malformed-hostile",
            "probe-failure-mode",
            "count_probes",
            "done",
        ]


