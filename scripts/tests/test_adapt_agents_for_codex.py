class TestEmitAgentToml:
    def test_starts_with_marker_comment(self) -> None:
        result = _emit_agent_toml("my-agent", "Short desc.", "sonnet", "Body text.")
        assert result.startswith(_MARKER + "\n")

    def test_contains_name_field(self) -> None:
        result = _emit_agent_toml("my-agent", "Short desc.", "sonnet", "Body text.")
        assert 'name = "my-agent"' in result

    def test_contains_description_field(self) -> None:
        result = _emit_agent_toml("my-agent", "Short desc.", "sonnet", "Body text.")
        assert 'description = "Short desc."' in result

    def test_contains_model_field(self) -> None:
        result = _emit_agent_toml("my-agent", "Short desc.", "opus", "Body text.")
        assert 'model = "opus"' in result

    def test_contains_developer_instructions(self) -> None:
        result = _emit_agent_toml("my-agent", "Short desc.", "sonnet", "Body content here.")
        assert 'developer_instructions = """' in result
        assert "Body content here." in result

    def test_escapes_triple_quotes_in_body(self) -> None:
        body = 'Some """quoted""" text.'
        result = _emit_agent_toml("my-agent", "Short desc.", "sonnet", body)
        assert '"""quoted"""' not in result.split('developer_instructions = """')[1].rstrip('"\n')

    def test_escapes_double_quotes_in_description(self) -> None:
        result = _emit_agent_toml("my-agent", 'Say "hello".', "sonnet", "Body.")
        assert r'description = "Say \"hello\"."' in result

    def test_body_ends_with_closing_triple_quote(self) -> None:
        result = _emit_agent_toml("my-agent", "Short desc.", "sonnet", "Body.")
        assert result.endswith('"""\n')


