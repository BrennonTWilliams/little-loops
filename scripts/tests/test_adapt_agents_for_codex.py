class TestIdempotency:
    def test_rerun_with_apply_skips_unchanged(self, tmp_path: Path) -> None:
        _make_agent(tmp_path, "my-agent")
        codex_dir = tmp_path / ".codex" / "agents"

        # First run: write
        adapted1, skipped1, _ = _process_agents(
            tmp_path / "agents", codex_dir, apply=True, quiet=True, only=None
        )
        assert adapted1 == 1
        assert skipped1 == 0

        # Second run: should skip (identical content)
        adapted2, skipped2, errors2 = _process_agents(
            tmp_path / "agents", codex_dir, apply=True, quiet=True, only=None
        )
        assert adapted2 == 0
        assert skipped2 == 1
        assert errors2 == 0

    def test_rerun_produces_no_diff(self, tmp_path: Path) -> None:
        _make_agent(tmp_path, "my-agent")
        codex_dir = tmp_path / ".codex" / "agents"
        toml_path = codex_dir / "my-agent.toml"

        _process_agents(tmp_path / "agents", codex_dir, apply=True, quiet=True, only=None)
        first_content = toml_path.read_text()

        _process_agents(tmp_path / "agents", codex_dir, apply=True, quiet=True, only=None)
        assert toml_path.read_text() == first_content

    def test_changed_source_triggers_rewrite(self, tmp_path: Path) -> None:
        agent_md = _make_agent(tmp_path, "my-agent", description="Original description.")
        codex_dir = tmp_path / ".codex" / "agents"
        _process_agents(tmp_path / "agents", codex_dir, apply=True, quiet=True, only=None)

        # Update source
        agent_md.write_text(
            "---\nname: my-agent\ndescription: |\n  Updated description.\nmodel: sonnet\n"
            'tools: ["Read"]\n---\n\nNew body.'
        )

        adapted, skipped, _ = _process_agents(
            tmp_path / "agents", codex_dir, apply=True, quiet=True, only=None
        )
        assert adapted == 1
        assert skipped == 0
        content = (codex_dir / "my-agent.toml").read_text()
        assert 'description = "Updated description."' in content


