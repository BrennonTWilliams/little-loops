from little_loops.cli.adapt_agents_for_codex import (
    _emit_agent_toml,
    _extract_agent_frontmatter,
    _extract_short_desc,
    _process_agents,
    main_adapt_agents_for_codex,
)

_MARKER = "# generated by ll-adapt"


def _make_agent(
    tmp_path: Path,
    name: str,
    description: str = "Use when user asks for stuff.",
    model: str = "sonnet",
    body: str = "Agent instructions.",
) -> Path:
    agents_dir = tmp_path / "agents"
    agents_dir.mkdir(parents=True, exist_ok=True)
    agent_md = agents_dir / f"{name}.md"
    agent_md.write_text(
        f"---\nname: {name}\ndescription: |\n  {description}\nmodel: {model}\n"
        f'tools: ["Read"]\n---\n\n{body}'
    )
    return agent_md


def _make_agent_block_scalar(tmp_path: Path, name: str) -> Path:
    agents_dir = tmp_path / "agents"
    agents_dir.mkdir(parents=True, exist_ok=True)
    agent_md = agents_dir / f"{name}.md"
    agent_md.write_text(
        "---\nname: my-agent\ndescription: |\n  Use when asked to do something.\n"
        "  More context here.\n\n  Trigger keywords: foo, bar\nmodel: sonnet\n"
        "---\n\n# My Agent\n"
    )
    return agent_md


