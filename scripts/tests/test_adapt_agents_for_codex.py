class TestMarkerPreservation:
    def test_user_authored_file_not_overwritten(self, tmp_path: Path) -> None:
        _make_agent(tmp_path, "my-agent")
        codex_dir = tmp_path / ".codex" / "agents"
        codex_dir.mkdir(parents=True)

        user_content = "# user-authored content\nname = 'my-agent'\n"
        (codex_dir / "my-agent.toml").write_text(user_content)

        adapted, skipped, errors = _process_agents(
            tmp_path / "agents", codex_dir, apply=True, quiet=True, only=None
        )

        assert adapted == 0
        assert skipped == 1
        assert errors == 0
        assert (codex_dir / "my-agent.toml").read_text() == user_content

    def test_generated_file_can_be_updated(self, tmp_path: Path) -> None:
        agent_md = _make_agent(tmp_path, "my-agent", description="Original.")
        codex_dir = tmp_path / ".codex" / "agents"
        _process_agents(tmp_path / "agents", codex_dir, apply=True, quiet=True, only=None)

        # Simulate stale generated file by updating source
        agent_md.write_text(
            "---\nname: my-agent\ndescription: |\n  New desc.\nmodel: sonnet\n"
            'tools: ["Read"]\n---\n\nNew body.'
        )

        adapted, skipped, _ = _process_agents(
            tmp_path / "agents", codex_dir, apply=True, quiet=True, only=None
        )
        assert adapted == 1
        assert skipped == 0
        content = (codex_dir / "my-agent.toml").read_text()
        assert content.startswith(_MARKER)
        assert 'description = "New desc."' in content
