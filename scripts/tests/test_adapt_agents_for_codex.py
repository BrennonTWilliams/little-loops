class TestExtractAgentFrontmatter:
    def test_returns_dict_with_known_fields(self) -> None:
        text = "---\nname: my-agent\ndescription: Use when stuff.\nmodel: sonnet\n---\n# Body"
        fm = _extract_agent_frontmatter(text)
        assert fm is not None
        assert fm["name"] == "my-agent"
        assert fm["model"] == "sonnet"

    def test_no_frontmatter_returns_none(self) -> None:
        assert _extract_agent_frontmatter("# No frontmatter") is None

    def test_unclosed_frontmatter_returns_none(self) -> None:
        assert _extract_agent_frontmatter("---\nname: x\n") is None

    def test_single_line_description(self) -> None:
        text = "---\ndescription: Use when user asks for stuff.\n---\n# Body"
        assert _extract_short_desc(text) == "Use when user asks for stuff."

    def test_block_scalar_returns_first_line(self) -> None:
        text = (
            "---\ndescription: |\n  Use when asked to do something.\n  More context.\n---\n# Body"
        )
        assert _extract_short_desc(text) == "Use when asked to do something."

    def test_truncates_to_80_chars(self) -> None:
        long_desc = "A" * 100
        text = f"---\ndescription: {long_desc}\n---\n# Body"
        assert len(_extract_short_desc(text)) == 80

    def test_no_frontmatter_returns_empty(self) -> None:
        assert _extract_short_desc("# No frontmatter") == ""

    def test_no_description_field_returns_empty(self) -> None:
        text = "---\nname: my-agent\n---\n# Body"
        assert _extract_short_desc(text) == ""


