class TestRealAgentsIntegrationGuard:
    """After ll-adapt-agents-for-codex --apply, all real .codex/agents/*.toml must exist."""

    def test_all_real_agents_have_toml_files(self) -> None:
        agents_dir = Path(__file__).parent.parent.parent / "agents"
        codex_agents_dir = Path(__file__).parent.parent.parent / ".codex" / "agents"
        if not codex_agents_dir.exists():
            return
        for agent_md in sorted(agents_dir.glob("*.md")):
            agent_name = agent_md.stem
            out_toml = codex_agents_dir / f"{agent_name}.toml"
            assert out_toml.exists(), (
                f".codex/agents/{agent_name}.toml missing. Run: ll-adapt-agents-for-codex --apply"
            )

    def test_all_real_toml_files_have_marker(self) -> None:
        codex_agents_dir = Path(__file__).parent.parent.parent / ".codex" / "agents"
        if not codex_agents_dir.exists():
            return
        for toml_path in sorted(codex_agents_dir.glob("*.toml")):
            content = toml_path.read_text()
            assert content.startswith(_MARKER), (
                f".codex/agents/{toml_path.name}: missing marker comment on first line"
            )

    def test_all_real_toml_files_have_required_fields(self) -> None:
        codex_agents_dir = Path(__file__).parent.parent.parent / ".codex" / "agents"
        if not codex_agents_dir.exists():
            return
        for toml_path in sorted(codex_agents_dir.glob("*.toml")):
            content = toml_path.read_text()
            assert 'name = "' in content, f"{toml_path.name}: missing name field"
            assert 'description = "' in content, f"{toml_path.name}: missing description field"
            assert 'model = "' in content, f"{toml_path.name}: missing model field"
            assert 'developer_instructions = """' in content, (
                f"{toml_path.name}: missing developer_instructions field"
            )


