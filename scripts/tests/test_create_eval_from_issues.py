class TestEvalHarnessValidation:
    """Tests that generated YAML passes ll-loop validate (via load_and_validate)."""

    @pytest.fixture
    def loops_dir(self, tmp_path: Path) -> Path:
        loops = tmp_path / ".loops"
        loops.mkdir()
        return loops

    def _write_and_validate(
        self, loops_dir: Path, monkeypatch: pytest.MonkeyPatch, yaml_content: str, name: str
    ) -> list:
        """Write YAML to tmp .loops dir, chdir to parent, validate via CLI."""
        (loops_dir / f"{name}.yaml").write_text(yaml_content)
        monkeypatch.chdir(loops_dir.parent)

        with pytest.MonkeyPatch.context() as mp:
            mp.setattr(sys, "argv", ["ll-loop", "validate", name])
            from little_loops.cli import main_loop

            result = main_loop()
        return result

    def test_variant_a_passes_fsm_validation(
        self, loops_dir: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        """Variant A YAML passes FSM structural validation."""
        (loops_dir / "eval-harness-feat-919.yaml").write_text(VARIANT_A_YAML)
        monkeypatch.chdir(loops_dir.parent)

        fsm, _ = load_and_validate(loops_dir / "eval-harness-feat-919.yaml")
        errors = validate_fsm(fsm)
        hard_errors = [e for e in errors if e.severity == ValidationSeverity.ERROR]
        assert not hard_errors, f"Variant A FSM errors: {[str(e) for e in hard_errors]}"

    def test_variant_b_passes_fsm_validation(
        self, loops_dir: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        """Variant B YAML passes FSM structural validation."""
        (loops_dir / "eval-harness-feat-919-enh-950.yaml").write_text(VARIANT_B_YAML)
        monkeypatch.chdir(loops_dir.parent)

        fsm, _ = load_and_validate(loops_dir / "eval-harness-feat-919-enh-950.yaml")
        errors = validate_fsm(fsm)
        hard_errors = [e for e in errors if e.severity == ValidationSeverity.ERROR]
        assert not hard_errors, f"Variant B FSM errors: {[str(e) for e in hard_errors]}"

    def test_variant_a_via_cli(self, loops_dir: Path, monkeypatch: pytest.MonkeyPatch) -> None:
        """Variant A passes ll-loop validate CLI (exit 0)."""
        (loops_dir / "eval-harness-feat-919.yaml").write_text(VARIANT_A_YAML)
        monkeypatch.chdir(loops_dir.parent)

        with pytest.MonkeyPatch.context() as mp:
            mp.setattr(sys, "argv", ["ll-loop", "validate", "eval-harness-feat-919"])
