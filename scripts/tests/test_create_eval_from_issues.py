    def test_initial_state_is_execute(self, harness: dict) -> None:
        assert harness.get("initial") == "execute"

    def test_category_is_harness(self, harness: dict) -> None:
        assert harness.get("category") == "harness"

    def test_has_execute_state(self, harness: dict) -> None:
        states = harness.get("states", {})
        assert "execute" in states

    def test_execute_is_prompt_type(self, harness: dict) -> None:
        execute = harness["states"]["execute"]
        assert execute.get("action_type") == "prompt"

    def test_execute_routes_to_check_skill(self, harness: dict) -> None:
        execute = harness["states"]["execute"]
        assert execute.get("next") == "check_skill"

    def test_has_check_skill_state(self, harness: dict) -> None:
        assert "check_skill" in harness.get("states", {})

    def test_check_skill_has_llm_structured_evaluator(self, harness: dict) -> None:
        check_skill = harness["states"]["check_skill"]
        evaluate = check_skill.get("evaluate", {})
        assert evaluate.get("type") == "llm_structured"

    def test_check_skill_evaluate_has_prompt(self, harness: dict) -> None:
        check_skill = harness["states"]["check_skill"]
        prompt = check_skill.get("evaluate", {}).get("prompt", "")
        assert len(prompt) > 20, "llm_structured prompt must not be empty"

    def test_check_skill_routes_on_yes_to_done(self, harness: dict) -> None:
        check_skill = harness["states"]["check_skill"]
        assert check_skill.get("on_yes") == "done"

    def test_check_skill_routes_on_no_to_execute(self, harness: dict) -> None:
        check_skill = harness["states"]["check_skill"]
        assert check_skill.get("on_no") == "execute"

    def test_has_terminal_done_state(self, harness: dict) -> None:
        states = harness.get("states", {})
        assert "done" in states
        assert states["done"].get("terminal") is True

    def test_no_check_invariants(self, harness: dict) -> None:
        """Eval harnesses do not include check_invariants (diff size gate)."""
        states = harness.get("states", {})
        assert "check_invariants" not in states

    def test_no_check_stall(self, harness: dict) -> None:
        """Eval harnesses do not include check_stall."""
